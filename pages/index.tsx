import type { NextPage } from 'next'
import Head from 'next/head'
import { FC, Fragment, useEffect, useRef, useState } from 'react';
import { getTransactions } from '../lib/transactions';
import { Transaction } from '../types/transactions.types';
import SingleTransaction from '../components/SingleTransaction';
import NewTransaction from '../components/NewTransaction';

const Home: NextPage = () => {
  // state variables
  const [transactions, updateTransactions] = useState<Transaction[]>([]);
  // only fetch at the beginning
  useEffect(() => {
    // async function
    const getData = async () => {
      const data = await getTransactions();
      // update the state
      updateTransactions(data);
    }
    // call async function
    getData();
  }, []);

  const [isOpen, setIsOpen] = useState(false)
  const closeModal = () => {
    setIsOpen(false);
  }
  const openModal = () => {
    setIsOpen(true);
  }

  return (
    <>
      <Head>
        <title>Fire Expense</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div className="flex w-full justify-between px-4">
        <h2>Transactions</h2>
        <button
          type="button"
          onClick={openModal}
          className="px-4 py-2 text-sm font-medium rounded-md border border-slate-300 hover:border-indigo-300"
        >
          Add Transaction
        </button>
      </div>
      <div className="w-full divide-y divide-gray-200">
        {transactions.map((transaction) => (
          <SingleTransaction key={transaction.id} transaction={transaction} />
        ))}
      </div>
      <NewTransaction isOpen={isOpen} onClose={closeModal} />
    </>
  )
}

export default Home
